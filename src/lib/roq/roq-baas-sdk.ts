import { type CheckSelect, fetcher, marshal, makeUrl } from '@roq/client';
import type { Prisma, tutor, user } from './types';
import { DefaultArgs, GetFindResult } from './types/runtime/library';

export type RequestOptions = Record<string, any>;

interface TutorSDK {
  create: <
    T extends Prisma.tutorCreateArgs,
    R extends CheckSelect<T, tutor, Prisma.tutorGetPayload<T>>,
    U extends Omit<Prisma.tutorCreateArgs, 'data'> & {
      data: Omit<Prisma.tutorUncheckedCreateInput, 'user'>;
    },
  >(
    data: Prisma.SelectSubset<U, T>,
    options?: RequestOptions,
  ) => Promise<R | undefined>;

  createMany: <T extends Prisma.tutorCreateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.tutorCreateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  update: <
    T extends Prisma.tutorUpdateArgs,
    R extends Prisma.tutorGetPayload<T>,
    U extends Omit<Prisma.tutorUpdateArgs, 'data'> & {
      data: Omit<Prisma.tutorUncheckedUpdateInput, 'user'>;
    },
  >(
    data: Prisma.SelectSubset<U, T>,
    options?: RequestOptions,
  ) => Promise<R | undefined>;

  updateMany: <T extends Prisma.tutorUpdateManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.tutorUpdateManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  upsert: <T extends Prisma.tutorUpsertArgs, R extends Prisma.tutorGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.tutorUpsertArgs>,
    options?: RequestOptions,
  ) => Promise<R | undefined>;

  delete: <T extends Prisma.tutorDeleteArgs, R extends Prisma.tutorGetPayload<T>>(
    data: Prisma.SelectSubset<T, Prisma.tutorDeleteArgs>,
    options?: RequestOptions,
  ) => Promise<R | undefined>;

  deleteMany: <T extends Prisma.tutorDeleteManyArgs, R extends Prisma.BatchPayload>(
    args: Prisma.SelectSubset<T, Prisma.tutorDeleteManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findMany: <T extends Prisma.tutorFindManyArgs, R extends GetFindResult<Prisma.$tutorPayload<DefaultArgs>, T>[]>(
    args?: Prisma.SelectSubset<T, Prisma.tutorFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  count: <T extends Prisma.tutorCountArgs, R extends number>(
    args?: Prisma.SelectSubset<T, Prisma.tutorCountArgs>,
    options?: RequestOptions,
  ) => Promise<R>;

  findManyWithCount: <
    T extends Prisma.tutorFindManyArgs,
    R extends GetFindResult<Prisma.$tutorPayload<DefaultArgs>, T>[],
    C extends number,
  >(
    args?: Prisma.SelectSubset<T, Prisma.tutorFindManyArgs>,
    options?: RequestOptions,
  ) => Promise<{ data: R; count: C }>;

  findFirst: <T extends Prisma.tutorFindFirstArgs, R extends GetFindResult<Prisma.$tutorPayload<DefaultArgs>, T>>(
    args?: Prisma.SelectSubset<T, Prisma.tutorFindFirstArgs>,
    options?: RequestOptions,
  ) => Promise<R>;
}

